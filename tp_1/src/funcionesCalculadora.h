#ifndef FUNCIONESCALCULADORA_H_
#define FUNCIONESCALCULADORA_H_



#endif /* FUNCIONESCALCULADORA_H_ */

/**
 * @brief Suma dos numeros flotantes
 * @param primer valor a sumar
 * @param segundo valor a sumar
 * @return la suma de los parametros
 */
float sumar(float a, float b);

/**
 * @brief resta dos numeros flotantes
 * @param primer valor ingresado
 * @param segundo valor que se resta al primer valor
 * @return la resta de los parametros
 */
float restar(float a, float b);

/**
 * @brief divide dos numeros flotantes
 * @param dividendo
 * @param divisor
 * @return la division los parametros
 */
float dividir(float a, float b);

/**
 * @brief multiplica dos numeros flotantes
 * @param primer valor a multiplicar
 * @param segundo valor a multiplicar
 * @return la multiplicacion de los parametros
 */
float multiplicar(float a, float b);

/**
 * @brief factorea el numero flotante ingresado y verifica si el numero ingresado es negativo
 * @param valor a factorear
 * @return devuelve -1 si el numero ingresado era negativo o devuelve el factorial resuelto si era >=0
 */
float factorear(float a);
